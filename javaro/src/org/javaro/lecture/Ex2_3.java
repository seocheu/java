package org.javaro.lecture;

public class Ex2_3 {

	public static void main(String[] args) {
		float a;
		float b = 2.1f;
		int c = 2;
		a = b + c;
		System.out.println("b = 2.1f; c = 2; a = b + c = " + a);
		a = b - c;
		System.out.println("b = 2.1f; c = 2; a = b - c = " + a);
		a = b * c;
		System.out.println("b = 2.1f; c = 2; a = b * c = " + a);
		a = b / c;
		System.out.println("b = 2.1f; c = 2; a = b / c = " + a);
		a = b % c;
		System.out.println("b = 2.1f; c = 2; a = b % c = " + a);
	}

}
